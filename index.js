let nameField = document.getElementById('nameField');
let title = document.getElementById('title');

// Signin Function 

 function signin() {
    nameField.style.display='none';
    title.innerHTML ="Sign-in";
 }

//  SignUp Function 


 function signup() {
    nameField.style.display='flex';
    title.innerHTML ="Sign-up";
    
 }

    

